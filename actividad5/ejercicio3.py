dia = int(input("Ingrese el dia de la semana de forma numerica (1-7): "))
while dia !=0:
    if dia==1:
        print("lunes")
    elif dia==2:
        print ("martes")
    elif dia==3:
        print ("miercoles")
    elif dia==4:
        print ("jueves")
    elif dia==5:
        print ("viernes")
    elif dia==6:
        print ("sabado")
    elif dia==7:
        print ("domingo")
    else:
        print("Error, el numero digitado no es valido")

print("Preciona 0 para salir")
